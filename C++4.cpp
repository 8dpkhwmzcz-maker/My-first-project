# include <iostream>
#include <cmath>
using namespace std;

int main() {
    cout << "The square root of 16 is: " << sqrt(16) << endl;
    cout << "The pwoer of 2 raised to 3 is: " << pow(2 , 3) << endl;
    cout << "The floor value of 5.7 is: " << floor(5.7) << endl;
    cout << "The ceiling value of 7.2 is: " << ceil(7.2) << endl;
    cout << "The absolute value of -10 is: " << abs(-10) << endl;
    
}

